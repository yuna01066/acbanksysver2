const pickSizePrices = (
  source: Record<string, Record<string, number>>,
  allowedSizes: readonly string[],
): Record<string, Record<string, number>> => Object.fromEntries(
  Object.entries(source).map(([thickness, sizeData]) => [
    thickness,
    Object.fromEntries(
      allowedSizes
        .map(size => [size, sizeData[size]] as const)
        .filter(([, price]) => Number(price) > 0)
    ),
  ])
);

